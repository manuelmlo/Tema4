    private void jButtonsubirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsubirActionPerformed
        
        jLabelrojo.setBackground(defecto);
        jLabelambar.setBackground(defecto);
        jLabelverde.setBackground(defecto);
        
        semaforo1.subir();
        
        switch (semaforo1.getEstado()){
            case 0:
                jLabelrojo.setBackground(Color.RED);
                jCheckBoxparpadea.setSelected(semaforo1.isparpadenado());
                break;
            case 1:
                jLabelambar.setBackground(Color.ORANGE);
                jCheckBoxparpadea.setSelected(semaforo1.isparpadenado());
                break;
            case 2:
                jLabelverde.setBackground(Color.GREEN);
                jCheckBoxparpadea.setSelected(semaforo1.isparpadenado());
                break;
                    
        }
        
    }//GEN-LAST:event_jButtonsubirActionPerformed

