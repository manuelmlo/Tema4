    private void jButton1quehoraesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1quehoraesActionPerformed
 
        Reloj reloj1= new Reloj(Byte.valueOf(jTextFieldhoras.getText()), 
                Byte.valueOf(jTextFieldminutos.getText()), 
                Byte.valueOf(jTextFieldsegundos.getText()));
        
        if(reloj1.getFormatohora()){
            jRadioButtonAMPM.setSelected(false);
            jRadioButton24horas.setSelected(true);
        }else{
            jRadioButtonAMPM.setSelected(true);
            jRadioButton24horas.setSelected(false);
            
        }
        
    }//GEN-LAST:event_jButton1quehoraesActionPerformed

