    private void jButtonbombillaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonbombillaActionPerformed
        
        Aparato bombilla= new Aparato("Bombilla",100);
        if(!bombilla.isEstado()){
            jCheckBoxestadobombilla.setText("Apagado");
            jCheckBoxestadobombilla.setSelected(false);
        }else{
            jCheckBoxestadobombilla.setText("Encendido");
            jCheckBoxestadobombilla.setSelected(true);
        }
        jTextAreaconsumo.append(bombilla.toString()+"\n");
        estado1=bombilla.isEstado();
        
    }//GEN-LAST:event_jButtonbombillaActionPerformed

